    private void jSaveAccountDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveAccountDetailsActionPerformed
     
    ISAAccount ISA = new ISAAccount();
    CurrentAccount Current = new CurrentAccount();
    SavingAccount Saving = new SavingAccount();

    String aSortCode = jASortCodeTextField.getText();
    int aAccountNo = Integer.parseInt(jAccountNoTextField.getText());
    double aBalance = Double.parseDouble(jABalanceTextField.getText());
    String aNameOfBank = jANoBankTextField.getText();  
    double aRate = Double.parseDouble(jARateTextField.getText());
    String aLastReportedDate = jALRDTextField.getText();

    // Clear existing text in jAccountsTextArea
    jAccountsTextArea.setText("");

    if (jISARadioBtn.isSelected()) {
        ISA.create(aSortCode, aAccountNo, aBalance, aNameOfBank, aRate, aLastReportedDate);
        ISA.SaveToFile();
        ISA.display(jAccountsTextArea);
    } else if (jCurrentRadioBtn.isSelected()) {
        Current.create(aSortCode, aAccountNo, aBalance, aNameOfBank, aRate, aLastReportedDate);
        Current.SaveToFile();
        Current.display(jAccountsTextArea);
    } else if (jSavingRadioBtn.isSelected()) {
        Saving.create(aSortCode, aAccountNo, aBalance, aNameOfBank, aRate, aLastReportedDate);
        Saving.SaveToFile();
        Saving.display(jAccountsTextArea);
    } else {
        jStatusMessageLabel.setText("Please pick a type of Account...");
    }
    
    // Set status message
    jStatusMessageLabel.setText("Updated and Saved Account details...");

    }//GEN-LAST:event_jSaveAccountDetailsActionPerformed


    private void jLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoginButtonActionPerformed
        // TODO add your handling code here:
        if (theUser.isUser(jNameTextField.getText(), jPasswordTextField.getText())) {
            jStatusMessageLabel.setText(jNameTextField.getText() + " is logged in...");

            // Check the user's role
            if (theUser.isManager()) {
                // Bank Manager
                jMainTabbedPane.addTab("Calculator", jTabInMemoryforCalculator);
                jMainTabbedPane.addTab("Head Office", jTabInMemoryforDisplay);
                jMainTabbedPane.addTab("Edit Head Office Details", jTabInMemoryforEdit);
                jMainTabbedPane.addTab("Clients", jTabInMemoryforDisplayClients);
                jMainTabbedPane.addTab("Branches", jTabInMemoryforDisplayBranch);
                jMainTabbedPane.addTab("Accounts", jTabInMemoryforAccounts);
            } else if (theUser.isEmployee()) {
                // Bank Employee
                jMainTabbedPane.addTab("Calculator", jTabInMemoryforCalculator);
                jMainTabbedPane.addTab("Head Office", jTabInMemoryforDisplay);
                jMainTabbedPane.addTab("Clients", jTabInMemoryforDisplayClients);
                jMainTabbedPane.addTab("Branches", jTabInMemoryforDisplayBranch);
                jMainTabbedPane.addTab("Accounts", jTabInMemoryforAccounts);
            }
            else if (theUser.isCustomer()) {
                //Bank Customer
                jMainTabbedPane.addTab("Calculator", jTabInMemoryforCalculator);
                jMainTabbedPane.addTab("Head Office", jTabInMemoryforDisplay);
            }
            else if (theUser.isAdvisor()) {
                //Bank Advisor
                jMainTabbedPane.addTab("Calculator", jTabInMemoryforCalculator);
                jMainTabbedPane.addTab("Head Office", jTabInMemoryforDisplay);
               
            }
            else if (theUser.isAdmin()) {
                //Bank Administrator
                jMainTabbedPane.addTab("Calculator", jTabInMemoryforCalculator);
                
            }
        } else {
            jStatusMessageLabel.setText("Cannot login, please check login and password");
        }
    }//GEN-LAST:event_jLoginButtonActionPerformed

